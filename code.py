def somme_deux_nombres(a, b):
    """
    Cette fonction prend deux nombres en entrée (a et b) et retourne leur somme.
    """
    somme = a + b
    return somme

# Test de la fonction
nombre1 = 10
nombre2 = 5
resultat = somme_deux_nombres(nombre1, nombre2)
print(f"La somme de {nombre1} et {nombre2} est égale à {resultat}.")
